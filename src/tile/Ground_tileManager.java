    public void draw(Graphics2D g) {
        int worldcol=0;
        int worldrow=0;


        int count=0;
        while (worldcol<GamePanel.maxworldcol&&worldrow<GamePanel.maxworldrow) {
        
            int worldx= worldcol*2560;
            int worldy= worldrow*GamePanel.titlesize;
            int screenx=worldx- entity.worldx + Player.screenx;
            int screeny=worldy- entity.worldy + Player.screeny;
            count++;
            if (worldx+GamePanel.titlesize>entity.worldx-Player.screenx -2600 &&worldx-GamePanel.titlesize<entity.worldx+Player.screenx +1100&&
                    worldy+GamePanel.titlesize>entity.worldy-Player.screeny -500&&worldy-GamePanel.titlesize<entity.worldy+Player.screeny+500) {


                switch (map[count]) {

                    case 1:
                        g.drawImage(GamePanel.images.get("Ground"), screenx-350+GamePanel.screen_move, screeny-14, 2570, 710, null);

                        break;
                    case 2:
                        g.drawImage(GamePanel.images.get("Ground_1"), screenx-350+GamePanel.screen_move, screeny-14, 2570, 710, null);

                        break;
                   
                }

            }
            worldcol++;
            


            if (worldcol==GamePanel.maxworldcol) {
                worldcol=0;
                worldrow++;


            }

        }



    }
